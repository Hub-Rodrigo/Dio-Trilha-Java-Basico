public class TiposVariaveis { 
    public static void main(String[] args) {
		
        //exemplos de varivel como novos valores
        int numero = 5;
        numero =10;
        System.out.print("Novo valor da Variavel é = " + numero +"\n");
        
        //exmeplos de varivel que nunca vai mudar acompanhada do final
        final double VALOR_DE_PI = 3.14;
        System.out.print("Valor de PI é: " + VALOR_DE_PI);

        
    }
    
}